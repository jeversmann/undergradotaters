char *convert_escape_sequences(char *string)
{
  char *read, *write;
  int actual_read, val;

  write = read = string;

  while (*read != 0) {
    if (*read == '\\') {
      val = parse_char_constant(read, &actual_read);
      if (val < 0) {
	*write++ = 'X';
      } else {
	*write++ = (char) val;
      }
      read += actual_read;
    } else {
      *write++ = *read++;
    }
  }
  *write = 0;

  return(string);
}

